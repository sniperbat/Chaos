#include "ChsEntity.h"
#include "ChsModel.h"

//--------------------------------------------------------------------------------------------------
namespace Chaos {

 	//------------------------------------------------------------------------------------------------
	ChsEntity::ChsEntity( const std::string & name ) : ChsRenderNode( name ) {
	}
	
  //------------------------------------------------------------------------------------------------
	ChsEntity::~ChsEntity( void ){
	}
	
  //------------------------------------------------------------------------------------------------
	void ChsEntity::update( double timeInterval ){
		if( this->model.expired() )
			return;
		const boost::shared_ptr<ChsModel> & model = this->model.lock();
		model->update( timeInterval );
	}
	
  //------------------------------------------------------------------------------------------------
}

