#include "ChsEntity.h"
#include "ChsModel.h"

//--------------------------------------------------------------------------------------------------
namespace Chaos {

 	//------------------------------------------------------------------------------------------------
	ChsEntity::ChsEntity( std::string name ) : ChsRenderNode( name ) {
	}
	
  //------------------------------------------------------------------------------------------------
	ChsEntity::~ChsEntity( void ){
	}
	
  //------------------------------------------------------------------------------------------------
	void ChsEntity::update( void ){
		if( this->model.expired() )
			return;
		boost::shared_ptr<ChsModel> model = this->model.lock();
		model->update();
	}
	
  //------------------------------------------------------------------------------------------------
}
